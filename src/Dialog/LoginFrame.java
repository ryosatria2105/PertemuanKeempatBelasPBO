    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
     String user = txtUser.getText().trim();
    String pw   = new String(txtPass.getPassword());  // <- JPasswordField

    if (user.isEmpty() || pw.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Isi username dan password dulu.");
        return;
    }

    var em = JPAUtil.getEntityManager();
    em.getTransaction().begin();

    Akun akun = em.find(Akun.class, user);

    if (akun == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Username tidak ditemukan.");
        txtUser.requestFocus();
    } else if (!akun.getPassword().equals(pw)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Password salah.");
        txtPass.setText("");
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Anda berhasil login !");

        new MemberFrame().setVisible(true); // masuk ke aplikasi kamu
        this.dispose();
    }

    em.getTransaction().commit();
    em.close();      // TODO add your handling code here:
    }//GEN-LAST:event_btnLoginActionPerformed

