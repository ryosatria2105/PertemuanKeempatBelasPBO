    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
     String user = txtUser.getText().trim();

    if (user.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan username terlebih dahulu!");
        return;
    }

    var em = JPAUtil.getEntityManager();
    Akun a = em.find(Akun.class, user);
    em.close();

    if (a == null) {
        JOptionPane.showMessageDialog(this, "Username tidak ditemukan!");
        return;
    }

        // Simpan jawaban aslinya
       correctAnswer = a.getSecurityAnswer();


    // Load pertanyaan
    if (a.getQuestionId() == 0) {
        txtQuestion.setText(a.getCustomQuestion());
    } else {
        txtQuestion.setText(getPertanyaanById(a.getQuestionId()));
    }

    txtAnswer.setEnabled(true);
    txtAnswer.requestFocus();
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        String user = txtUser.getText().trim();
    String jawaban = txtAnswer.getText().trim();
    String pwBaru = new String(txtPassBaru.getPassword());
    String pwKonfirmasi = new String(txtPassKonfirmasi.getPassword());

    // Cek field kosong
    if (user.isEmpty() || pwBaru.isEmpty() || pwKonfirmasi.isEmpty() || jawaban.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
        return;
    }

    // Cek konfirmasi password
    if (!pwBaru.equals(pwKonfirmasi)) {
        JOptionPane.showMessageDialog(this, 
                "Password baru dan konfirmasi password tidak sama!");
        return;
    }

    // Cek jawaban security question
    if (correctAnswer == null || !jawaban.equalsIgnoreCase(correctAnswer)) {
        JOptionPane.showMessageDialog(this, "Jawaban keamanan salah!");
        return;
    }

    var em = JPAUtil.getEntityManager();
    em.getTransaction().begin();

    Akun a = em.find(Akun.class, user);
    if (a == null) {
        JOptionPane.showMessageDialog(this, "Username tidak ditemukan!");
        em.getTransaction().rollback();
        em.close();
        return;
    }

    // Update password baru
    a.setPassword(pwBaru);

    em.getTransaction().commit();
    em.close();

    JOptionPane.showMessageDialog(this, "Password berhasil diperbarui!");
    new LoginFrame().setVisible(true);
    this.dispose();
    }//GEN-LAST:event_btnUbahActionPerformed

