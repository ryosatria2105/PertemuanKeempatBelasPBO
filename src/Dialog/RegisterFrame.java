    private void btnBuatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuatActionPerformed
    String user = txtUser.getText().trim();
    String pw   = new String(txtPass.getPassword());
    String cpw  = new String(txtConfirmPassword.getPassword());
    String jawaban = txtJawaban.getText().trim();
    String customQ = txtCustomQuestion.getText().trim();
    int selectedIndex = cbQuestion.getSelectedIndex();

    // Validasi field wajib
    if (user.isEmpty() || pw.isEmpty() || cpw.isEmpty() || jawaban.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
        return;
    }

    // Cek password sama
    if (!pw.equals(cpw)) {
        JOptionPane.showMessageDialog(this, 
            "Password dan Konfirmasi Password tidak sama!");
        return;
    }

    // Validasi logika pertanyaan
    Integer questionId;
    boolean pilihList = selectedIndex != -1;
    boolean buatCustom = !customQ.isEmpty();

    if (!pilihList && !buatCustom) {
        JOptionPane.showMessageDialog(this,
            "Pilih pertanyaan dari daftar atau isi pertanyaan custom!");
        return;
    }

    if (pilihList && buatCustom) {
        JOptionPane.showMessageDialog(this,
            "Pilih salah satu: daftar ATAU custom question!");
        return;
    }

    if (pilihList) {
        questionId = selectedIndex + 1;
        customQ = null;
    } else {
        questionId = 0;
    }

    var em = JPAUtil.getEntityManager();
    em.getTransaction().begin();

    // Cek username sudah ada atau tidak
    Akun cek = em.find(Akun.class, user);
    if (cek != null) {
        JOptionPane.showMessageDialog(this,
            "Username sudah dipakai! Gunakan username lain.");
        em.getTransaction().rollback();
        em.close();
        return;
    }

    // Insert akun baru
    Akun a = new Akun();
    a.setUsername(user);
    a.setPassword(pw); // *Next: kita ganti jadi hashed password üîê*
    a.setQuestionId(questionId);
    a.setSecurityAnswer(jawaban);
    a.setCustomQuestion(customQ);

    em.persist(a);
    em.getTransaction().commit();
    em.close();

    JOptionPane.showMessageDialog(this, "Berhasil Daftar!");
    new LoginFrame().setVisible(true);
    this.dispose();
    }//GEN-LAST:event_btnBuatActionPerformed

